import re

phone_number = input()
pattern = "\\+359-2-[0-9]{3}-[0-9]{4}\\b|\\+359 2 [0-9]{3} [0-9]{4}\\b"
matches = re.findall(pattern, phone_number)
print(", ".join(matches))

